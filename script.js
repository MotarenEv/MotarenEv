// Intersection Observer for fade-in animations
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('visible');
        }
    });
}, observerOptions);

// Add fade-in class to elements and observe them
document.addEventListener('DOMContentLoaded', () => {
    const elementsToAnimate = document.querySelectorAll('.product-card, .feature-card, .stat, .about-text, .section-header');
    elementsToAnimate.forEach(el => {
        el.classList.add('fade-in');
        observer.observe(el);
    });
});

// Counter animation for stats
function animateCounter(element, target, duration = 2000) {
    let start = 0;
    const increment = target / (duration / 16);
    
    function updateCounter() {
        start += increment;
        if (start < target) {
            element.textContent = Math.floor(start).toLocaleString();
            requestAnimationFrame(updateCounter);
        } else {
            element.textContent = target.toLocaleString();
        }
    }
    
    updateCounter();
}

// Animate counters when they come into view
const counterObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const counters = entry.target.querySelectorAll('.stat h4');
            counters.forEach(counter => {
                const text = counter.textContent;
                if (text.includes('%')) {
                    const value = parseFloat(text.replace('%', ''));
                    animateCounter(counter, value, 2000);
                    counter.textContent = value.toFixed(1) + '%';
                } else if (text.includes('+')) {
                    const value = parseInt(text.replace(/[^\d]/g, ''));
                    animateCounter(counter, value, 2000);
                    counter.textContent = value.toLocaleString() + '+';
                } else if (text.includes('Years')) {
                    const value = parseInt(text.replace(/[^\d]/g, ''));
                    animateCounter(counter, value, 2000);
                    counter.textContent = value + ' Years';
                }
            });
            counterObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

// Observe stats section for counter animation
document.addEventListener('DOMContentLoaded', () => {
    const statsSection = document.querySelector('.about-stats');
    if (statsSection) {
        counterObserver.observe(statsSection);
    }
});

